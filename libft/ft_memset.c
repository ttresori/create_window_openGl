#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	void	*r;

	i = 0;
	r = s;
	while (i < n)
	{
		*(unsigned char *)s = c;
		i++;
		s++;
	}
	return (r);
}
