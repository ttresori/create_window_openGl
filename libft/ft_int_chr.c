#include "libft.h"

int	ft_int_chr(const int *tab, int nb, int len)
{
	int i;

	i = 0;
	if (!tab || len < 1)
		return (-1);
	while (i < len)
	{
		if (tab[i] == nb)
			return (1);
		i++;
	}
	return (0);
}
